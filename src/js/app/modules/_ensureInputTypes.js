// Contact Form - Change .input-type-[type] type attribute
if ($(".input-type-tel").length) {
  $(".input-type-tel").attr("type", "tel");
}
if ($(".input-type-email").length) {
  $(".input-type-email").attr("type", "email");
}
if ($(".input-type-number").length) {
  $(".input-type-number").attr("type", "number");
}
